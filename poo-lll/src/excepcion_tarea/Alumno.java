
package excepcion_tarea;

public class Alumno 
{
    String nombre;
    int no_cuenta;
    int edad;

    public String getNombre() 
    {
        return nombre;
    }

    public void setNombre(String nombre) 
    {
        this.nombre = nombre;
    }

    public int getNo_cuenta() 
    {
        return no_cuenta;
    }

    public void setNo_cuenta(int no_cuenta) 
    {
        this.no_cuenta = no_cuenta;
    }

    public int getEdad() 
    {
        return edad;
    }

    public void setEdad(int edad) 
    {
        this.edad = edad;
    }

    public Alumno() {
    }
     
}
